nome = str(input('Qual é seu nome? '))
if nome == 'Vinicius':
    print('Que nome bonito!')
elif nome == 'Caneta':
    print('É azul?')
elif nome == 'Pedro' or nome == 'Maria' or nome == 'Paulo':
    print('Seu nome é bem popular no Brasil!')

else:
    print('Tenha um bom dia, {}!'.format(nome))
